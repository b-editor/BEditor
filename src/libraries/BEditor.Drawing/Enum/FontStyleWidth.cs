namespace BEditor.Drawing
{
    /// <summary>
    /// Defines the font widths to be used in <see cref="Font"/>.
    /// </summary>
    public enum FontStyleWidth
    {
        /// <summary>
        /// A condensed font width of 1.
        /// </summary>
        UltraCondensed = 1,

        /// <summary>
        /// A condensed font width of 2.
        /// </summary>
        ExtraCondensed = 2,

        /// <summary>
        /// A condensed font width of 3.
        /// </summary>
        Condensed = 3,

        /// <summary>
        /// A condensed font width of 4.
        /// </summary>
        SemiCondensed = 4,

        /// <summary>
        /// A normal font width of 5. This is the default font width.
        /// </summary>
        Normal = 5,

        /// <summary>
        /// An expanded font width of 6.
        /// </summary>
        SemiExpanded = 6,

        /// <summary>
        /// An expanded font width of 7.
        /// </summary>
        Expanded = 7,

        /// <summary>
        /// An expanded font width of 8.
        /// </summary>
        ExtraExpanded = 8,

        /// <summary>
        /// An expanded font width of 9.
        /// </summary>
        UltraExpanded = 9,
    }
}